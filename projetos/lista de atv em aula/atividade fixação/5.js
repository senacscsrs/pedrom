let fruta = "laranja"

switch (fruta) {
    case "banana":
        console.log("Esta fruta é a banana")
        break;
        case "uva":
            console.log("Esta é a uva")
            break;
            case "maçã":
                console.log("Esta fruta é uma maçã")
                break;
                default:
                    console.log("Fruta não encontrada")
                    break;
}